/**
 * 根据给定的字符串，算出显示的宽度和高度
 */
void ui_font_width_height(int display_width, ui_font_t * font, const char * title,
        int * width, int * height) {
    int r_width = 0, r_height = font->height;
    int max_width = 0;

    if (!title) {
        *width = 0;
        *height = 0;
        return;
    }

    while (*title++) {
        r_width += font->width;

        if (r_width > display_width) {
            r_height += font->height;
            r_width = font->width;
            max_width = display_width;
        }
    }

    if (max_width && width) {
        *width = max_width;
    } else {
        *width = r_width;
    }

    if (height) {
    	*height = r_height;
    }
}
